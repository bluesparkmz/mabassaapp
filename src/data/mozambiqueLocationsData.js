export const provinceOptions = mozambiqueProvinces.map((province) => province.name);

export function getDistrictsByProvince(provinceName) {
  return (
    mozambiqueProvinces.find((province) => province.name === provinceName)?.districts || []
  );
}

export function getDefaultDistrict(provinceName) {
  return getDistrictsByProvince(provinceName)[0] || "";
}
